# Known overrides for cities that are already past 'queued'.
# Maps (country_slug, city_slug) -> {"status": <city-level>, "categories": <"all" or {cat: status}>, "note": <str>}.
# "all" means every category gets the given status.
KNOWN_STATE = {
    ("france", "paris"): {
        "status": "shipped",
        "all_categories": "deployed",
        "note": "worked example; shipped 2026-05-17",
    },
    ("japan", "tokyo"): {
        "status": "scaffolded",
        "all_categories": "queued",
        "note": "pre-US-priority leftover stub; refill after Tier 1 complete",
    },
}


def build():
    cities = []
    for (country_slug, city_slug, display_name, country_name, tier, priority) in ROWS:
        # Default everyone is queued.
        city_status = "queued"
        categories = {t: "queued" for t in TOPICS}
        note = None
        override = KNOWN_STATE.get((country_slug, city_slug))
        if override:
            city_status = override.get("status", city_status)
            if override.get("all_categories"):
                categories = {t: override["all_categories"] for t in TOPICS}
            elif override.get("categories"):
                for cat, st in override["categories"].items():
                    if cat in categories:
                        categories[cat] = st
            note = override.get("note")
        entry = {
            "country_slug": country_slug,
            "city_slug": city_slug,
            "display_name": display_name,
            "country_name": country_name,
            "tier": tier,
            "priority": priority,
            "status": city_status,
            "categories": categories,
        }
        if note:
            entry["note"] = note
        cities.append(entry)

    payload = {
        "_meta": {
            "schema_version": "tj.tracker.v1",
            "description": (
                "Single source of truth for TableJourney work tracking. Every "
                "(country, city, category) tuple has a status field. Research "
                "subagents update categories as they fill JSON. Validation "
                "subagents flip the status to 'validated' after PASS. "
                "ship_city.sh flips it to 'deployed' once HTML is live. "
                "The orchestrator reads this file to know where to resume "
                "after any session ends. See docs/ORCHESTRATOR.md for the "
                "loop semantics."
            ),
            "category_status_values": [
                "queued",       # not started
                "researching",  # research subagent currently filling
                "researched",   # JSON populated, awaiting validation
                "validating",   # validation agent currently reviewing
                "validated",    # validation PASS, awaiting ship
                "needs_fixes",  # validation flagged issues; back to research
                "deployed",     # HTML live at the public URL
            ],
            "city_status_values": [
                "queued",        # not started
                "scaffolded",    # new_city.py has run; JSON stubs exist
                "in_progress",   # at least one category is past queued
                "ready_to_ship", # every entity category is validated
                "shipped",       # every category is deployed
            ],
            "topics_in_order": TOPICS,
            "tiers": {
                "1": "United States (highest editorial + ad-rate priority)",
                "2": "European Union + UK + Ireland + Nordics + Switzerland",
                "3": "Asia (Japan, Greater China, SE Asia, South Asia)",
                "4": "Americas non-US (Mexico, Canada, South America, Caribbean)",
                "5": "Middle East + Africa + Oceania",
            },
            "totals": {
                "cities": len(cities),
                "categories_per_city": len(TOPICS),
                "tracker_rows": len(cities) * len(TOPICS),
            },
            "how_agents_update_this_file": [
                "1. Read the whole file.",
                "2. Find your city (by country_slug + city_slug).",
                "3. Update the appropriate category status. Do NOT touch other cities.",
                "4. Write the whole file back atomically (write to .tmp then os.replace).",
                "5. The orchestrator may bump the city-level status based on category statuses.",
            ],
        },
        "cities": cities,
    }
    return payload


def main():
    out = Path(__file__).resolve().parent
    payload = build()
    target = Path("/station/repo/data/locations.json")
    target.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    cities = payload["cities"]
    print(f"Wrote {target}")
    print(f"  cities:          {len(cities)}")
    print(f"  topics per city: {len(TOPICS)}")
    print(f"  tracker rows:    {len(cities) * len(TOPICS)}")
    tier_counts = {}
    for c in cities:
        tier_counts[c["tier"]] = tier_counts.get(c["tier"], 0) + 1
    print(f"  by tier:         {tier_counts}")
    status_counts = {}
    for c in cities:
        status_counts[c["status"]] = status_counts.get(c["status"], 0) + 1
    print(f"  by status:       {status_counts}")

    # Dupe check
    keys = [(c["country_slug"], c["city_slug"]) for c in cities]
    dupes = [k for k in set(keys) if keys.count(k) > 1]
    if dupes:
        print(f"  DUPLICATES: {dupes}")
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
